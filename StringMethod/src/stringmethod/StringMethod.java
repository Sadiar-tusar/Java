
package stringmethod;


public class StringMethod {

   
    public static void main(String[] args) {
     String massege="Welcome To Java ";
     
        System.out.println("Length= "+massege.length());
        
        System.out.println("Char At= "+massege.charAt(2));
        
         System.out.println("Concate= "+massege.concat("Hello"));
         
          System.out.println("To Upper Case= "+massege.toUpperCase());
          
          System.out.println("To Lower Case= "+massege.toLowerCase());
          
           System.out.println("Trim= "+massege.trim());
        
    }
    
}
